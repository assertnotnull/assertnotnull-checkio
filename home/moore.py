def count_neighbours(grid, row, col):
    count = 0
    for x in range(-1, 2):
        for y in range(-1, 2):
            if x == 0 and y == 0:
                continue
            try:
                if row + x >= 0 and col + y >= 0:
                    count += grid[row + x][col + y]
            except IndexError:
                pass
    return count


